
class Solution:
    #Your task is to complete this function
    #Function should return an integer
    #a - list/array containing height of stack's respectively
    def max_Books(self, n, k, arr):
        max_val = -1
        count = 0
        for i in arr:
            if i <= k:
                count += i
            else:
                max_val = max(max_val, count)
                count = 0
        max_val = max(max_val, count)
        return max_val


