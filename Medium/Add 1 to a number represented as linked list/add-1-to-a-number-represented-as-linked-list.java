class Solution
{
    public static Node addOne(Node head) 
    { 
        Node temp = reverse(head);
        Node newHead = temp;
        int add = 0, carry = 1;
        while(temp != null){
            add = temp.data + carry;
            carry = add / 10;
            add %= 10;
            temp.data = add;
            temp = temp.next;
        }
        
        if(carry > 0){
            Node carryNode = new Node(carry);
            carryNode.next = reverse(newHead);
            temp = carryNode;
        }
        else{
            temp = reverse(newHead);
        }
        return temp;
    }
    
    static Node reverse(Node temp){
        Node prev = null;
        Node curr = temp;
        Node next = null;
        
        while(curr != null){
            next = curr.next;
            curr.next = prev;
            prev = curr;
            curr = next;
        }
        return prev;
    }
}
