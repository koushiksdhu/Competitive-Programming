class Solution
{
   public static Node findIntersection(Node head1, Node head2)
    {
        ArrayList<Integer> hs1 = new ArrayList<>();
        ArrayList<Integer> hs2 = new ArrayList<>();
        
        Node temp = head1;
        while(temp != null){
            hs1.add(temp.data);
            temp = temp.next;
        }
        
        temp = head2;
        while(temp != null) {
            hs2.add(temp.data);
            temp = temp.next;
        }
        
        Node ans = new Node(-1);
        Node h = ans;
        
        for(int i : hs1) {
            if(hs2.contains(i)){
                h.next = new Node(i);
                hs2.remove(hs2.indexOf(i));
                h = h.next;
            }
        }
        return ans.next;
    }
}